public class Arrs {
    public static void main(String[] args) {
        String[] cars = {"Volo", "BMW", "Ford", "Mazda"};
        for (String car : cars) {
            System.out.println(car);
        }

        char[] car = {'V', 'o', 'l', 'o'};
        for (char c : car) {
            System.out.println(c);
        }
    }
}
